    private void uploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadActionPerformed
        // TODO add your handling code here:
        File file = null;
        FileInputStream fis = null;
        ObjectInputStream ois = null;
       
        try {
            JFileChooser jfc = new JFileChooser("./");
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Nuila te amo", "cathy");
            jfc.setFileFilter(filtro);
            int seleccion = jfc.showOpenDialog(this);
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                file = jfc.getSelectedFile();
                fis = new FileInputStream(file);
                ois = new ObjectInputStream(fis);
                Deporte dep = (Deporte) ois.readObject();
                for (Torneo torneo : dep.getTorneos()) {
                    ArrayList<Equipo> es = torneo.getEquipos();
                    for (Equipo equip : es) {
                        if (!equipos.contains(equip)) {
                            equipos.add(equip);
                        }
                    }
                }
                llenarTree(dep);
            } 

        } catch (Exception e) {
            e.printStackTrace();
        }
        try {
            ois.close();
            fis.close();
        } catch (IOException ex) {
        }

                                          
    }//GEN-LAST:event_uploadActionPerformed

