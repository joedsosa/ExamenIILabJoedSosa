    public void Save(Deporte sport) {
        JFileChooser jfc = new JFileChooser("./");
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Torneo docs", "cathy");
        jfc.setFileFilter(filtro);
        int seleccion = jfc.showSaveDialog(this);

        FileOutputStream fo = null;
        ObjectOutputStream oo = null;
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            try {

                File fichero = null;
                fichero = new File(jfc.getSelectedFile().getPath() + ".cathy");

                fo = new FileOutputStream(fichero);
                oo = new ObjectOutputStream(fo);
                oo.writeObject(sport);
                oo.flush();
                ProgressBarThread p = new ProgressBarThread(jp_pro, sport);
                p.start();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }//fin IF
        try {
            fo.close();
            oo.close();
            
        } catch (IOException ex) {
        }
    }

