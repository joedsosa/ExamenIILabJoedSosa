    private void jTree2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree2MouseClicked
        // TODO add your handling code here:

        model = (DefaultTreeModel) jTree2.getModel();
        root = (DefaultMutableTreeNode) model.getRoot();
        int row = jTree2.getClosestRowForLocation(evt.getX(), evt.getY());
        jTree2.setSelectionRow(row);
        Object v1 = jTree2.getSelectionPath().getLastPathComponent();
        node = (DefaultMutableTreeNode) v1;
        if (evt.isMetaDown()) {
            if (node.getUserObject().equals("Torneos")) {
                pop_team.show(evt.getComponent(), evt.getX(), evt.getY());
            } else if (node.getUserObject() instanceof String) {
                pop_quarters.show(evt.getComponent(), evt.getX(), evt.getY());
            }

            if (node.getUserObject() instanceof Deporte deporte) {
                deportes = deporte;
                pop_deportes.show(evt.getComponent(), evt.getX(), evt.getY());
            }
            if (node.getUserObject() instanceof Torneo torneo) {
                torneos = torneo;
                pop_torneos.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        }

    }//GEN-LAST:event_jTree2MouseClicked

