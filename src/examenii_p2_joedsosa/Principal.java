    private void jb_guardarresulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_guardarresulActionPerformed
        // TODO add your handling code here:
        if (cb_equipo1.getSelectedItem().equals(cb_equipo2.getSelectedItem())) {
            JOptionPane.showMessageDialog(this, "NO PUEDE SER EL MISMO EQUIPO!!");
        } else {
            Equipo team = ((Equipo) cb_equipo1.getSelectedItem());
            Equipo scnteam= ((Equipo) cb_equipo2.getSelectedItem());
            if ((int) js_equipo1.getValue() > (int) js_equipo2.getValue()) {
                team.contpunt(3);
            } else {
                scnteam.contpunt(3);
            }
            if ((int) js_equipo1.getValue() == (int) js_equipo2.getValue()) {
                team.contpunt(1);
                scnteam.contpunt(1);
            }
            Partido partido = new Partido();
            Equipo[] e = {team, scnteam};
            partido.setEquipos(e);
            Integer[] r = {(int) js_equipo1.getValue(), (int) js_equipo2.getValue()};
            partido.setResultados(r);
            team.addPartido(partido);
            scnteam.addPartido(partido);
            JOptionPane.showMessageDialog(this, "GANO EL MEJOR");
        }
    }//GEN-LAST:event_jb_guardarresulActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
         if (jList1.getSelectedIndex() >= 0) {
            DefaultListModel model = (DefaultListModel) jList1.getModel();
            Equipo equipo = ((Equipo) model.getElementAt(jList1.getSelectedIndex()));
            DefaultTableModel modell = (DefaultTableModel) jTable1.getModel();
            modell.setRowCount(0);
            for (Partido partido : equipo.getPartido()) {
                String[] pp = {partido.getEquipos()[0].toString(), partido.getEquipos()[1].toString(), partido.getResultados()[0].toString() + " ## " + partido.getResultados()[1].toString()};
                modell.addRow(pp);
            }
            modell.fireTableDataChanged();

        }
    }//GEN-LAST:event_jList1MouseClicked

